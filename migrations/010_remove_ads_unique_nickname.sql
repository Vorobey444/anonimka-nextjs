-- Миграция 010: Удаление уникального constraint для nickname в таблице ads
-- Дата: 2025-11-10
-- Проблема: Пользователь не может создать несколько объявлений, т.к. nickname должен быть уникальным
-- Решение: Удаляем constraint unique_nickname, т.к. один пользователь может иметь много объявлений

-- Удаляем уникальный constraint для nickname
ALTER TABLE ads DROP CONSTRAINT IF EXISTS unique_nickname;

-- Информация о миграции
COMMENT ON TABLE ads IS 'Таблица объявлений. Один пользователь может создавать несколько объявлений с одним никнеймом.';
